package cn.tarena.weather.utils;

public class Xingqi {
	public static String nx(int str) {
		switch (str) {
		case 1:
			return "一";
		case 2:
			return "二";
		case 3:
			return "三";
		case 4:
			return "四";
		case 5:
			return "五";
		case 6:
			return "六";
		case 7:
			return "日";
		}
		return "六";
	}
}